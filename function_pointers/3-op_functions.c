#include "3-calc.h"

/**
 * op_add - suma
 *
 * @a: int
 * @b: int
 *
 * Description: suma a y b
 *
 * Return: suma
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - resta
 *
 * @a: int
 * @b: int
 *
 * Description: resta a y b
 *
 * Return: resta
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplica
 *
 * @a: int
 * @b: int
 *
 * Description: multiplica a y b
 *
 * Return: multiplica
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide
 *
 * @a: int
 * @b: int
 *
 * Description: divide a y b
 *
 * Return: divide
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 *
 * @a: int
 * @b: int
 *
 * Description: modulo a y b
 *
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a + b);
}
